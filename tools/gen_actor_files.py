def vAddrToPAddr(vAddr):
    return vAddr - 0x100000

def getActorSize(actorId):
    code.seek(vAddrToPAddr(gActorOverlayTableVAddr) + (actorId * 0x20) + 0x14)
    actorInitInfoVAddr = struct.unpack('<i', code.read(4))[0]
    code.seek(vAddrToPAddr(actorInitInfoVAddr) + 0xC)
    actorSize = struct.unpack('<i', code.read(4))[0]
    return actorSize

# typedef struct {
#     /* 0x00 */ s16 id;
#     /* 0x02 */ u8  type; // Classifies actor and determines when actor will execute
#     /* 0x04 */ u32 flags;
#     /* 0x08 */ s16 objectId;
#     /* 0x0C */ u32 instanceSize;
#     /* 0x10 */ ActorFunc init; // Constructor
#     /* 0x14 */ ActorFunc destroy; // Destructor
#     /* 0x18 */ ActorFunc update; // Main Update Function
#     /* 0x1C */ ActorFunc draw; // Draw function
# } ActorInit; // size = 0x20

def getActorInitVars(actorId):
    code.seek(vAddrToPAddr(gActorOverlayTableVAddr) + (actorId * 0x20) + 0x14)
    actorInitInfoVAddr = struct.unpack('<i', code.read(4))[0]
    code.seek(vAddrToPAddr(actorInitInfoVAddr))
    id = struct.unpack('<h', code.read(2))[0]
    type = struct.unpack('<B', code.read(1))[0]
    pad = struct.unpack('<b', code.read(1))[0]
    flags = struct.unpack('<I', code.read(4))[0]
    objectId = struct.unpack('<h', code.read(2))[0]
    pad = struct.unpack('<h', code.read(2))[0]
    instanceSize = struct.unpack('<I', code.read(4))[0]
    init = struct.unpack('<I', code.read(4))[0]
    destroy = struct.unpack('<I', code.read(4))[0]
    update = struct.unpack('<I', code.read(4))[0]
    draw = struct.unpack('<I', code.read(4))[0]
    return (id, type, flags, objectId, instanceSize, init, destroy, update, draw)

def createActorHeader(path, actorName, actorId):
    actorStructName = actorName.replace("_","")
    actorSize = getActorSize(actorId)

    fileStr = "#ifndef _Z_" + actorName.upper() + "_H_\n"
    fileStr += "#define _Z_" + actorName.upper() + "_H_\n"
    fileStr += "\n"
    fileStr += "#include \"global.hpp\"\n"
    fileStr += "\n"
    fileStr += "struct " + actorStructName + ";\n"
    fileStr += "\n"
    fileStr += "typedef struct " + actorStructName + " {\n"
    fileStr += "    /* 0x0000 */ Actor actor;\n"
    if actorSize > 0x1A4:
        fileStr += "    /* 0x01A4 */ char unk_1A4[" + "0x{:X}".format(actorSize - 0x1A4) + "];\n"
    fileStr += "} " + actorStructName + "; // size = " + "0x{:X}".format(actorSize) + "\n"
    fileStr += "\n"
    fileStr += "extern ActorInit " + actorName + "_InitVars;\n"
    fileStr += "\n"
    fileStr += "#endif\n"

    with open(path, 'w') as header:
        header.write(fileStr)

def createActorSource(path, actorName, actorId):
    addrs = open('function_addresses_new.txt', 'r')
    funcs = addrs.read()

    linkerFile = open('oot_new.ld', 'r')
    linker = linkerFile.read()

    functionNamesToRemove = []

    actorStructName = actorName.replace("_","")
    initVars = getActorInitVars(actorId)

    fileStr = "extern \"C\" {\n"
    if actorId != 0:
        fileStr += "#include \"z_" + actorName.lower() + ".hpp\"\n"
    else:
        fileStr += "#include \"global.hpp\""
    fileStr += "\n"
    fileStr += "#define FLAGS 0x{:08X}\n".format(initVars[2])
    fileStr += "\n"
    if (initVars[5] != 0):
        fileStr += "void " + actorStructName + "_Init(Actor* actor, GameState* state);\n"
    if (initVars[6] != 0):
        fileStr += "void " + actorStructName + "_Destroy(Actor* actor, GameState* state);\n"
    if (initVars[7] != 0):
        fileStr += "void " + actorStructName + "_Update(Actor* actor, GameState* state);\n"
    if (initVars[8] != 0):
        fileStr += "void " + actorStructName + "_Draw(Actor* actor, GameState* state);\n"
    fileStr += "\n"
    fileStr += "/*\n"
    fileStr += "ActorInit " + actorName + "_InitVars = {\n"
    fileStr += "    " + actorsTable[initVars[0]] + ",\n"
    fileStr += "    " + actorCategories[initVars[1]] +",\n"
    fileStr += "    FLAGS,\n"
    fileStr += "    " + objectsTable[initVars[3]] + ",\n"
    fileStr += "    sizeof(" + actorStructName + "),\n"
    if (initVars[5] != 0):
        fileStr += "    (ActorFunc)" + actorStructName + "_Init,\n"
    else:
        fileStr += "    NULL,\n"
    if (initVars[6] != 0):
        fileStr += "    (ActorFunc)" + actorStructName + "_Destroy,\n"
    else:
        fileStr += "    NULL,\n"
    if (initVars[7] != 0):
        fileStr += "    (ActorFunc)" + actorStructName + "_Update,\n"
    else:
        fileStr += "    NULL,\n"
    if (initVars[8] != 0):
        fileStr += "    (ActorFunc)" + actorStructName + "_Draw,\n"
    else:
        fileStr += "    NULL,\n"
    fileStr += "};\n"
    fileStr += "*/\n"

    if (initVars[5] != 0):
        fileStr += "\nGLOBAL_ASM(\"binary/" + actorStructName + "_Init.o\")\n"
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + actorStructName + "_Init.o\")\n")
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + "FUN_" + "{:08x}".format(initVars[5]) + ".o\")\n")
        funcs = funcs.replace("FUN_" + "{:08x}".format(initVars[5]), actorStructName + "_Init")
        linker = linker.replace("FUN_" + "{:08x}".format(initVars[5]), actorStructName + "_Init")
    if (initVars[6] != 0):
        fileStr += "\nGLOBAL_ASM(\"binary/" + actorStructName + "_Destroy.o\")\n"
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + actorStructName + "_Destroy.o\")\n")
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + "FUN_" + "{:08x}".format(initVars[6]) + ".o\")\n")
        funcs = funcs.replace("FUN_" + "{:08x}".format(initVars[6]), actorStructName + "_Destroy")
        linker = linker.replace("FUN_" + "{:08x}".format(initVars[6]), actorStructName + "_Destroy")
    if (initVars[7] != 0):
        fileStr += "\nGLOBAL_ASM(\"binary/" + actorStructName + "_Update.o\")\n"
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + actorStructName + "_Update.o\")\n")
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + "FUN_" + "{:08x}".format(initVars[7]) + ".o\")\n")
        funcs = funcs.replace("FUN_" + "{:08x}".format(initVars[7]), actorStructName + "_Update")
        linker = linker.replace("FUN_" + "{:08x}".format(initVars[7]), actorStructName + "_Update")
    if (initVars[8] != 0):
        fileStr += "\nGLOBAL_ASM(\"binary/" + actorStructName + "_Draw.o\")\n"
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + actorStructName + "_Draw.o\")\n")
        functionNamesToRemove.append("GLOBAL_ASM(\"binary/" + "FUN_" + "{:08x}".format(initVars[8]) + ".o\")\n")
        funcs = funcs.replace("FUN_" + "{:08x}".format(initVars[8]), actorStructName + "_Draw")
        linker = linker.replace("FUN_" + "{:08x}".format(initVars[8]), actorStructName + "_Draw")

    addrs.close()
    with open('function_addresses_new.txt', 'w') as newFuncs:
        newFuncs.write(funcs)
    linkerFile.close()
    with open('oot_new.ld', 'w') as newLinker:
        newLinker.write(linker)

    fileStr += "}\n"

    with open('funcsToRemove.txt', 'a') as removes:
        for func in functionNamesToRemove:
            removes.write(func)

    with open(path, 'w') as source:
        source.write(fileStr)

